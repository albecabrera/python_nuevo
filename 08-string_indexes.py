name = "Ricardo"
print(name)
print(name[0])
print(name[1])
print(name[-1])
print(name[0:4]) # 0 to 4
print(name[0:4:2]) # 0 to 4 with step of 2
print(name[::2]) # 0 to end with step of 2
print(name[::-1]) # reverse string

